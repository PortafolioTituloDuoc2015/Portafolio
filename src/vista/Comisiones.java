    private void btnCincoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCincoActionPerformed
        ComisionesDao dao = new ComisionesDao();
        Date fecha = this.calendarFecha.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy"); 
        int totalPorciento = dao.comisionDiariaCincoPorCiento(sdf.format(fecha));
        int total = dao.comisionTotalDia(sdf.format(fecha));
        this.txtTotal.setText(Integer.toString(total));
        this.txtTotalPorciento.setText(Integer.toString(totalPorciento));
        
    }//GEN-LAST:event_btnCincoActionPerformed

