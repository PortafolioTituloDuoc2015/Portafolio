    private void btnMensualDiezActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMensualDiezActionPerformed
        ComisionesDao dao = new ComisionesDao();
        String mes = Integer.toString(cmbMes.getMonth()+1);
        String año = Integer.toString(cmbAno.getYear()).substring(2, 4);
        int total = dao.comisionTotalMes(mes, año);
        int totalPorciento = dao.comisionMesDiezPorCiento(mes, año);
        this.txtTotalPorciento.setText(Integer.toString(totalPorciento));
        this.txtTotal.setText(Integer.toString(total));
        
    }//GEN-LAST:event_btnMensualDiezActionPerformed

